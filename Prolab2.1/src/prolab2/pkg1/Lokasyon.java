package prolab2.pkg1;
public class Lokasyon {
	private int x, y;
	
	public Lokasyon() {
		x = 5;
		y = 6;
	}
	
	public Lokasyon(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
}
